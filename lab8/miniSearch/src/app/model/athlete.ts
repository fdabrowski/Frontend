export class Athlete {

    firstName: string;
    secondName: string;
    age: number;
    dyscipline: string;
    club: string;

    constructor(firstName: string, secondName: string, age: number, dyscipline: string, club: string) {
        this.firstName = firstName;
        this.secondName = secondName;
        this.age = age;
        this.dyscipline = dyscipline;
        this.club = club;
    }
}
